// File: chardefs.h
// Purpose: Character glyph definitions
//
// This file uses a series of macros so that it can group the pixel bits of
// each character together, visually, in this source file, while still being
// able to group scan lines together, physically, in flash memory. In this
// source file, each character glyph is shown as a series of 8 macros,
// representing scan lines #0..#7 of that specific character. In the
// compiled binary, the "character_defs" array is arranged by scan line
// first, then by character index, so all of the scan line #0 bytes are
// together (for characters #00..#FF), followed by all of the scan line
// #1 bytes (for characters #00..#FF), and so on.
//

